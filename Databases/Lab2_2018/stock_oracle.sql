declare
  drop_sql long;
begin
  for rec in (select table_name 
              from   user_tables 
              where  table_name in ('DEMO_ORDER_ITEMS','DEMO_ORDERS','DEMO_CUSTOMERS','DEMO_PRODUCT_INFO')
             )
  loop
  begin
    drop_sql := 'drop table '||rec.table_name||'  CASCADE CONSTRAINTS purge';
    dbms_output.put_line(rec.table_name); 
    execute immediate drop_sql;
    EXCEPTION
      WHEN OTHERS THEN
           IF SQLCODE = -942 THEN
              DBMS_OUTPUT.PUT_LINE('Table Cannot be dropped');
              NULL;
           END IF;    
  end;
  end loop;          
END;
